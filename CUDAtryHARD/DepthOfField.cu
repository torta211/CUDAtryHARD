__global__ void gpu_show_depth(int* image_in, int* image_out, float* depth, int width, int height)
{
    unsigned int x = blockIdx.x * blockDim.x + threadIdx.x;
    unsigned int y = blockIdx.y * blockDim.y + threadIdx.y;

    float d = depth[y * width + x] - 0.875;

    int r = d * 8 * 255;
    int g = d * 8 * 255;
    int b = d * 8 * 255;

    image_out[y * width + x] = b << 16 | g << 8 | r;
}

__global__ void gpu_blur_image(int * image_in, int * image_out, float* depth, int width, int height, float focus)
{
    unsigned int x = blockIdx.x * blockDim.x + threadIdx.x;
    unsigned int y = blockIdx.y * blockDim.y + threadIdx.y;

    float d = depth[y * width + x];
    float d_rescaled = d > 0.875 ? (d - 0.875) * 8 : 0.0f;

    int kernel_radius = 15.0f * abs(d_rescaled - focus);

    int colFirst = x > kernel_radius? x - kernel_radius : 0;
    int colLast  = x + kernel_radius < width ? x + kernel_radius : width - 1;
    int rowFirst = y > kernel_radius ? y - kernel_radius : 0;
    int rowLast  = y + kernel_radius < height ? y + kernel_radius : height - 1;

    int num_elements_in_kernel = (colLast - colFirst + 1) * (rowLast - rowFirst + 1);

    int r_sum = 0;
    int g_sum = 0;
    int b_sum = 0;
    int offs;
    int pixel;

    for (int row = rowFirst; row <= rowLast; row++)
    {
        offs = row * width;
        for (int col = colFirst; col <= colLast; col++)
        {
            pixel = image_in[offs + col];
            r_sum += pixel & 0xff;
            g_sum += (pixel >> 8) & 0xff;
            b_sum += (pixel >> 16) & 0xff;
        }
    }

    int r = r_sum / num_elements_in_kernel;
    int g = g_sum / num_elements_in_kernel;
    int b = b_sum / num_elements_in_kernel;

    image_out[y * width + x] = b << 16 | g << 8 | r;
}

